#include "../../includes/scope.h"

t_vec2i   c_vec2i(int x, int y) {
  t_vec2i ret;

  ret.x = x;
  ret.y = y;
  return ret;
}

t_vec2f   c_vec2f(float x, float y) {
  t_vec2f ret;

  ret.x = x;
  ret.y = y;
  return ret;
}

t_vec3i   c_vec3i(int x, int y, int z) {
  t_vec3i ret;

  ret.x = x;
  ret.y = y;
  ret.z = z;
  return ret;
}

t_vec3f   c_vec3f(float x, float y, float z) {
  t_vec3f ret;

  ret.x = x;
  ret.y = y;
  ret.z = z;
  return ret;
}
